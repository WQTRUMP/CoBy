function rel(id, from, to, company, depth, relationType, description, risk, confidence, sourceIds, options = {}) {
  const relationScope = options.scope ?? inferRelationScope(from, to);
  return {
    id,
    from,
    to,
    company,
    depth,
    relationScope,
    relationType,
    description,
    risk,
    confidence,
    sourceIds,
    verification: options.verification ?? makeVerification(confidence, sourceIds, relationScope),
  };
}

function inferRelationScope(from, to) {
  const fromType = nodes[from]?.type;
  const toType = nodes[to]?.type;
  if (fromType === "company" && toType === "product") return "companyProduct";
  if (fromType === "product") return "productSupplier";
  return "supplierSupplier";
}

function makeVerification(confidence, sourceIds, relationScope) {
  const hasPrimary = sourceIds.some((sourceId) => ["财报", "公司公告", "供应商公告"].includes(sources[sourceId]?.type));
  const hasMedia = sourceIds.some((sourceId) => sources[sourceId]?.type === "媒体");
  const evidenceScore = confidence === "high" ? 86 : confidence === "medium" ? 64 : 42;
  const status = confidence === "high" && hasPrimary ? "verified" : confidence === "low" || hasMedia ? "challenged" : "review";
  const checklist = [
    hasPrimary ? "存在公司或供应商原始披露" : "需要补充公司或供应商原始披露",
    sourceIds.length > 1 ? "至少两条来源可交叉核对" : "当前证据来源数量偏少",
    relationScope === "supplierPeer" ? "该关系属于竞合/替代，不应解读为上下游采购合同" : "需确认关系范围、产品口径和是否仍在有效期",
  ];
  const doubts = [
    confidence !== "high" ? "关系强度或采购占比未被直接量化" : null,
    hasMedia ? "包含媒体来源，需用原始公告复核" : null,
    relationScope === "supplierPeer" ? "竞合关系可能随客户导入和产能切换快速变化" : null,
  ].filter(Boolean);

  return {
    status,
    evidenceScore,
    checklist,
    doubts,
    lastChecked: updatedAt,
    nextAction: status === "verified" ? "季度复核来源是否仍有效" : "补充原始披露、合同范围或产能验证证据",
  };
}

